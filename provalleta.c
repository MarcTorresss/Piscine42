#include <unistd.h>

void	doit(char *c)
{
	int	i;
	int j;

	j = 0;
	i = 0;
	while (c[i] != '\0')
	{
		if (c[i] >= 97 && c[i] <= 122)
		{
			j = c[i] - 96;
		}
		else if (c[i] >= 65 && c[i] <= 90)
		{
			j = c[i] - 64 ;
		}
		else 
			j = 0;
		while (j > 0)
		{
			write(1, &c[i], 1);
			j--;
		}
		i++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
		doit(argv[1]);
	else 
		write(1, "\n", 1);
}
